public class product { // -> Encapsulation files
    
    public String name;
    public String type;
    public String colour;
    public int manyString;

    
    public String getName(){
        return name;
    }

    public void setName(String newName){
        name = newName;
    }

    public String getType(){
        return type;
    }

    public void setType(String newType){
        type = newType;
    }

    public String getColour(){
        return colour;
    }

    public void setColour(String newColour){
        colour = newColour;
    }

    public int getManyString(){
        return manyString;
    }

    public void setManyString(int guitarString){
        manyString = guitarString;
    }

} 
